package day6.Casting;

public class Example {
  public static void main(String[] args) {
    // inivital values
    int i = 200;

    // cast to a short
    short s = (short) i;

    // cast to a double
    double d = (double) i;

    // cast to a byte
    byte b = (byte) i;

    System.out.println(s);
    System.out.println(d);
    System.out.println(b);
  }
}
